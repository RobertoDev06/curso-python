'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte 
Str -> String -> Texto
Strings são textos que estão dentro de apas
'''

print(1234)

# Aspas Simples
print('Paulo Roberto')

# Aspas Duplas
print("Calebe Ravi")

# Escape (Faz um pulo de caractere)
print("Paulo \"Roberto\"")  

# r (Mostra o Escape)
print(r"Paulo \"Roberto\"") 

# Bizu (Aspas Dupla podem está dentro de aspas Simples ou então também ao contrário)
print('Paulo "Roberto"')  
print(19, "anos e me chamo 'Paulo Roberto'")  
