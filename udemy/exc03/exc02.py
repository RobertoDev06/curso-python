# Operadores Aritméticos em Python

# Adição
adicao = 10 + 10
print('Adição', adicao)

# Subtração
subtracao = 10 - 5
print('Subtração', subtracao)

# Multiplicação
multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

# Divisão
divisao = 10 / 3
print('Divisão', divisao)

# Divisão inteira
divisao_inteira = 10 // 3
print('Divisão inteira', divisao_inteira)

# Exponenciação
exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

# Módulo (resto da divisão)
modulo = 55 % 2
print('Módulo', modulo)

# Testes com módulo (verificando se é divisível)
print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)