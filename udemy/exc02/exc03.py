print(int('1'), type(int('1')))  # Converte '1' (string) para inteiro e mostra o valor e o tipo
print(type(float('1') + 1))      # Converte '1' para float, soma com 1 e mostra o tipo do resultado
print(bool(' '))                 # Converte uma string com espaço para boolean (string não vazia = True)
print(str(11) + 'b')              # Converte 11 para string para poder concatenar com 'b'